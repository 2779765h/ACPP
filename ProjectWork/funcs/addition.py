def add(a,b):
  '''
  Function to add two numbers (a,b) and output the sum (a + b)
      
  Parameters
  ----------
  a: float
    input number
  b: float
    input number
        
  Returns
  ----------
  float
      sum of a + b
  '''
  print('Addition of {0} + {1} is {2}'.format(a, b, a + b))
  return a + b
