def product(a,b):
  '''
  Function which multiplies two numbers (a*b) and outputs the product

  Parameters
  ----------
  a: float
    input number
  b: float
    input number
        
  Returns
  ----------
  float
      number from the result of the product of a*b
  '''
  print('Multiplying {0} x {1} gives {2}'.format(a, b, a*b))
  return a*b
